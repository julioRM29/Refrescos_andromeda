public class Premio{ 
        
    String id_usuario, id_registro_premio, id_codigo, nombre_premio, usuario, puntos;

    public String getId_usuario() {
        return id_usuario;
    }

    public void setId_usuario(String id_usuario) {
        this.id_usuario = id_usuario;
    }

    public String getId_registro_premio() {
        return id_registro_premio;
    }

    public void setId_registro_premio(String id_registro_premio) {
        this.id_registro_premio = id_registro_premio;
    }

    public String getId_codigo() {
        return id_codigo;
    }

    public void setId_codigo(String id_codigo) {
        this.id_codigo = id_codigo;
    }

    public String getNombre_premio() {
        return nombre_premio;
    }

    public void setNombre_premio(String nombre_premio) {
        this.nombre_premio = nombre_premio;
    }

    public String getUsuario() {
        return usuario;
    }

    public void setUsuario(String usuario) {
        this.usuario = usuario;
    }

    public String getPuntos() {
        return puntos;
    }

    public void setPuntos(String puntos) {
        this.puntos = puntos;
    }
    
    
    
}
